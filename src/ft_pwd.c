#include "minishell.h"

int	ft_pwd(void)
{
	char *cwd;

	if (((cwd = getcwd(NULL, 0)) != NULL))
		ft_putstr(cwd);
	else
		write(1, "Error!", 7);
	write(1, "\n", 1);
	return (1);
}
